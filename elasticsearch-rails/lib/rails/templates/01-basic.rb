gsub_file 'app/controllers/articles_controller.rb', %r{# GET /articles/1$}, <<-CODE
  # GET /articles/search
  def search
    @articles = Article.search(params[:q]).records

    render :action => "index"
  end

  # GET /articles/1
CODE

